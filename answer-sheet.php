  <script>
    const radioStates = {};

    document.querySelectorAll('input[type="radio"]').forEach(radio => {
        radio.addEventListener('mousedown', function(e) {
            const name = this.name;
            // Store whether this radio was checked before the click
            radioStates[name] = radioStates[name] || null;
            if (this === radioStates[name]) {
                this.addEventListener('click', uncheck, { once: true });
            } else {
                radioStates[name] = this;
            }
        });

        function uncheck(e) {
            this.checked = false;
            radioStates[this.name] = null;
            e.preventDefault(); // Prevent normal click behavior
        }
    });
</script>

